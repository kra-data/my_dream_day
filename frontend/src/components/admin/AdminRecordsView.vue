      </div>
    </div>

    <!-- Main Content -->
    <div v-else class="records-section">
      <div class="section-header">
        <div class="header-info">
          <h2>📋 출퇴근 기록</h2>
          <p class="records-count">총 {{ attendanceStore.records.length }}건의 기록</p>
        </div>
        <div class="filters">
